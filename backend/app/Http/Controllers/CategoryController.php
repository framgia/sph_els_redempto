    public function getWordsBySlug($slug) {
        return Category::where('slug', $slug)->first()->words;
    }

    public function getWordsBySlugAndId($slug, $id) {
        $words = Category::where('slug', $slug)->first()->words;

        foreach($words as $word) {
            if ($word->id == $id) {
                return $word;
            }
        }

